// Helper function to get documents by tier
export const getDocumentsByTier = (tier) => {
    return Object.entries(documentTemplates)
        .filter(([_, doc]) => doc.tier === tier)
        .map(([id, doc]) => ({ id, ...doc }));
};

// Helper function to get all documents
export const getAllDocuments = () => {
    return Object.entries(documentTemplates)
        .map(([id, doc]) => ({ id, ...doc }));
};
